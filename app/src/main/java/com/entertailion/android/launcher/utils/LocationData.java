package com.entertailion.android.launcher.utils;

/**
 * Data structure to track location data.
 * 
 * @see http
 *      ://developer.android.com/training/basics/location/locationmanager.html
 * @see manifest: android.permission.ACCESS_COARSE_LOCATION
 * 
 * @author leon_nicholls
 * 
 */
public class LocationData {
	private double longitude;
	private double latitude;
	private String zipcode;
	private String countryName;
	private String countryCode;

	public LocationData() {

	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}

	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	public String getCountryCode() {
		return countryCode;
	}

	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
}
